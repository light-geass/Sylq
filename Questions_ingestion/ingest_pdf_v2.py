#  7. MAIN INGESTION
# ═══════════════════════════════════════════════════════════════════════════════

def ingest_pdf_v2(
    pdf_path: str,
    year: int,
    branch: str,
    answer_pdf: str | None = None,
    is_pyq: bool = True,
    dry_run: bool = False,
):
    print(f"\n{'='*65}")
    print(f"  GATE Ingestion v2")
    print(f"  PDF       : {pdf_path}")
    print(f"  Year      : {year}  |  Branch: {branch}")
    print(f"  Mode      : {'PYQ' if is_pyq else 'Non-PYQ (AI solves)'}")
    print(f"  Answer PDF: {answer_pdf or 'N/A'}")
    print(f"  Dry run   : {dry_run}")
    print(f"{'='*65}\n")

    # ── 1. Extract full text ──────────────────────────────────────────────────
    with pdfplumber.open(pdf_path) as pdf:
        total_pages = len(pdf.pages)
    print(f"  Pages: {total_pages}")
    all_text = extract_best_page_text(pdf_path)

    if not all_text.strip():
        print("  ERROR: No text extracted. PDF may be scanned — use ingest_image.py.")
        return

    # ── 2. Parse marks from section headers ───────────────────────────────────
    marks_map = parse_marks_map(all_text)
    one_mk = sum(1 for v in marks_map.values() if v == 1)
    two_mk = sum(1 for v in marks_map.values() if v == 2)
    print(f"  Marks map: {one_mk} × 1-mark  +  {two_mk} × 2-mark  =  {one_mk + two_mk*2} total")

    # ── 3. Load answer key (PYQ only) ─────────────────────────────────────────
    answer_key: dict = {}
    if is_pyq and answer_pdf:
        answer_key = parse_answer_key_pdf(answer_pdf)
    elif is_pyq and not answer_pdf:
        print("  [WARNING] PYQ mode but no --answer-pdf supplied.")
        print("  Pass: --answer-pdf gate_DA2025_answer.pdf")
        print("  Or use --no-pyq for AI-solved mode.")
        return

    # ── 4. Extract embedded images (diagrams) ────────────────────────────────
    print("\n  Extracting embedded images …")
    page_images = extract_images_from_pdf(pdf_path)
    total_imgs = sum(len(v) for v in page_images.values())
    print(f"  Found {total_imgs} diagram(s) across {len(page_images)} page(s)")

    # ── 5. Build page → question-number map ──────────────────────────────────
    page_qnums = build_page_question_map(pdf_path)

    # ── 6. Parse questions from text ─────────────────────────────────────────
    questions = extract_questions_from_text(all_text)
    print(f"\n  Parsed {len(questions)} questions")

    # ── 7. Associate images with questions ───────────────────────────────────
    q_images = associate_images(questions, page_images, page_qnums, pdf_path)
    print(f"  Questions with figures (embedded + fallback snapshots): {len(q_images)}")

    # ── 8. Process each question ─────────────────────────────────────────────
    inserted = failed = skipped = 0

    for q_data in tqdm(questions, desc="Processing"):
        qn = q_data["number"]

        # — Marks —
        marks = marks_map.get(qn, 1)

        # — Answer —
        if is_pyq:
            ans = answer_key.get(qn)
            if not ans:
                print(f"\n  [Q.{qn}] No answer in key — skipping")
                skipped += 1
                continue
            if ans == "MTA":
                print(f"\n  [Q.{qn}] MTA (Marks to All) — skipping")
                skipped += 1
                continue
        else:
            ans = "PENDING"   # AI will fill this later

        # — Determine Structure —
        is_structured = True
        if qn in q_images:
            is_structured = False
        else:
            # Check for complex math symbols, nested matrix brackets, or massive underscore gaps
            import re
            if re.search(r"_{3,}|\[\s*\[|∫|∑|∏", q_data["text"]):
                is_structured = False

        # — Build QuestionIn —
        try:
            q = QuestionIn(
                question_blocks = [make_text_block(q_data["text"])],
                question_type  = q_data["question_type"],
                options        = q_data["options"],
                correct_answer = ans,
                marks          = marks,
                branch_code    = branch,
                is_pyq         = is_pyq,
                pyq_year       = year if is_pyq else None,
                question_number = qn,
                is_structured  = is_structured,
            )
        except Exception as e:
            print(f"\n  [Q.{qn}] Validation error: {e}")
            failed += 1
            continue

        # — AI step —
        if is_pyq:
            q = tag_with_ai(q)              # answer already known → just tag
        else:
            q = solve_and_tag_with_ai(q)    # AI finds answer + tags

        # — Figure upload —
        image_url = None
        if qn in q_images:
            fig = q_images[qn]
            url = upload_question_image(fig, year, branch, qn, dry_run)
            if url:
                image_url = url
                q.add_image_url(url)

        # — Preview —
        print(f"\n  Q.{qn}  [{q.question_type.value}]  {marks}mk  [{q.difficulty.value}]")
        print(f"    Subject : {q.subject_name}")
        print(f"    Topic   : {q.topic_name}")
        print(f"    Answer  : {q.correct_answer}")
        print(f"    Figure  : {image_url or 'None'}")
        # Safe print for Windows console
        safe_text = q.get_plain_text()[:90].encode('ascii', 'replace').decode()
        print(f"    Text    : {safe_text}...")

        if dry_run:
            print("    [DRY RUN] skipped insert")
            inserted += 1
            time.sleep(0.2)
            continue

        # — Insert —
        result = insert_question(q)
        if result:
            inserted += 1
        else:
            failed += 1

        time.sleep(2)   # Groq rate-limit safety

    # Summary
    print(f"\n{'='*65}")
    print(f"  DONE")
    print(f"  Inserted : {inserted}")
    print(f"  Skipped  : {skipped}")
    print(f"  Failed   : {failed}")
    print(f"{'='*65}\n")


