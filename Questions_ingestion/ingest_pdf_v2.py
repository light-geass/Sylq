# Gate PDFs have recurring images on every/most pages:
#   - 2036x2037 JPEG  = watermark / background (on ALL 34 pages)
#   - 146x146   PNG   = GATE logo (on first ~9 pages)
# These must be filtered out. Only unique-sized images are real diagrams.

def _find_recurring_image_sizes(doc: fitz.Document, threshold: int = 5) -> set[tuple[int, int]]:
    """
    Scan all pages and find image sizes that appear on >= `threshold` pages.
    Those are watermarks / logos / repeated branding — not question diagrams.
    """
    size_count: dict[tuple[int, int], int] = {}
    for page in doc:
        seen_on_page: set[tuple[int, int]] = set()
        for img_info in page.get_images(full=True):
            try:
                base = doc.extract_image(img_info[0])
                size = (base["width"], base["height"])
                if size not in seen_on_page:
                    seen_on_page.add(size)
                    size_count[size] = size_count.get(size, 0) + 1
            except Exception:
                pass
    return {s for s, c in size_count.items() if c >= threshold}


def extract_images_from_pdf(pdf_path: str) -> dict[int, list[dict]]:
    """
    Extract embedded images from each page using PyMuPDF.
    Automatically filters out watermarks, logos, and branding images
    that appear on many pages (detected by recurring dimensions).

    Returns: {page_num: [{"bytes": b"...", "ext": "png", "rect": Rect, ...}, ...]}
    """
    page_images: dict[int, list[dict]] = {}
    doc = fitz.open(pdf_path)

    # First pass: identify recurring images (watermarks/logos)
    recurring = _find_recurring_image_sizes(doc, threshold=5)
    if recurring:
        print(f"    Filtered recurring images (watermark/logo): {recurring}")

    # Second pass: extract only real diagram images
    for page_num in range(len(doc)):
        page = doc[page_num]
        images_on_page = []

        for img_info in page.get_images(full=True):
            xref = img_info[0]
            try:
                base = doc.extract_image(xref)
                w, h = base["width"], base["height"]

                # Skip tiny images (bullets, decorations) based on area to allow wide/short diagrams
                if w * h < 4000:
                    continue

                # Skip recurring watermarks/logos
                if (w, h) in recurring:
                    continue

                # Get position of this image on the page
                rects = page.get_image_rects(xref)
                rect  = rects[0] if rects else None

                images_on_page.append({
                    "bytes": base["image"],
                    "ext":   base["ext"],
                    "width": w,
                    "height": h,
                    "rect":  rect,
                })
            except Exception as e:
                print(f"    [WARN] Image xref={xref} page {page_num+1}: {e}")

        if images_on_page:
            page_images[page_num + 1] = images_on_page

    doc.close()
    return page_images


def upload_question_image(
    fig: dict, year: int, branch: str, q_num: str, dry_run: bool
) -> str | None:
    """Upload a single figure to Supabase Storage and return the public URL."""
    if dry_run:
        return f"[DRY-RUN] would upload {year}/{branch}/Q{q_num}.{fig['ext']}"

    ext  = fig.get("ext", "png")
    path = f"{year}/{branch}/Q{q_num}_fig.{ext}"
    ctype = "image/png" if ext == "png" else "image/jpeg"
    return upload_to_storage(fig["bytes"], path, content_type=ctype)


