#  3. ANSWER KEY PDF PARSER
# ═══════════════════════════════════════════════════════════════════════════════

def parse_answer_key_pdf(answer_pdf_path: str) -> dict[str, str | list[str]]:
    """
    Extract answers from a GATE answer key PDF.

    The real format (Gate_DA2025_answerkey.pdf) is a table:
        Q.No  Session  Q.Type  Section  Key/Range  Marks
        1     5        MCQ     GA       A          1
        24    5        MSQ     DA       A;B;C      1
        31    5        NAT     DA       0.25 to 0.25  1
        62    5        NAT     DA       MTA        2

    Handles:
        MCQ  →  "A"
        MSQ  →  ["A","B","C"]  (from "A;B;C")
        NAT  →  "0.25"          or range "0.285 to 0.287" → midpoint
        MTA  →  "MTA"           (Marks to All)
    """
    answer_key: dict[str, str | list[str]] = {}

    all_text = ""
    with pdfplumber.open(answer_pdf_path) as pdf:
        for page in pdf.pages:
            text = page.extract_text()
            if text:
                all_text += text + "\n"

    if not all_text.strip():
        print(f"  [ERROR] No text in answer key PDF: {answer_pdf_path}")
        return answer_key

    # Each data row: "N  5  TYPE  SECTION  KEY/RANGE  MARKS"
    # Pattern: start-of-line number, then session(5), type, section, then the KEY
    row_pattern = re.compile(
        r"^(\d{1,2})\s+"              # Q number
        r"\d+\s+"                      # Session (always 5, ignore)
        r"(MCQ|MSQ|NAT)\s+"            # Question type
        r"(?:GA|DA)\s+"                # Section (ignore)
        r"(.+?)\s+"                    # Key/Range (capture greedily up to marks)
        r"(\d)\s*$",                   # Marks (1 or 2)
        re.MULTILINE | re.IGNORECASE,
    )

    for m in row_pattern.finditer(all_text):
        q_num  = m.group(1)
        q_type = m.group(2).upper()
        raw    = m.group(3).strip()

        # MTA → Marks to All
        if raw.upper() == "MTA" or raw.lower().startswith("marks to"):
            answer_key[q_num] = "MTA"

        # MSQ → "A;B;C" → ["A", "B", "C"]
        elif q_type == "MSQ" and re.search(r"[A-D]", raw):
            answer_key[q_num] = [c.strip().upper() for c in re.split(r"[;,]", raw) if c.strip()]

        # NAT → "0.25 to 0.25" or "0.285 to 0.287" → midpoint
        elif q_type == "NAT":
            if "to" in raw.lower():
                parts = re.split(r"\s*to\s*", raw, flags=re.I)
                try:
                    lo, hi = float(parts[0]), float(parts[1])
                    answer_key[q_num] = str(round((lo + hi) / 2, 6))
                except (ValueError, IndexError):
                    answer_key[q_num] = raw
            else:
                answer_key[q_num] = raw

        # MCQ → single letter "A"
        elif q_type == "MCQ" and raw.upper() in ("A", "B", "C", "D"):
            answer_key[q_num] = raw.upper()

        # Fallback
        else:
            answer_key[q_num] = raw

    print(f"  Loaded {len(answer_key)} answers from answer key PDF")

    # Quick sanity check
    if answer_key:
        mcq = sum(1 for v in answer_key.values() if isinstance(v, str) and v in "ABCD")
        msq = sum(1 for v in answer_key.values() if isinstance(v, list))
        nat = sum(1 for v in answer_key.values() if isinstance(v, str) and re.match(r"[\d.]", v))
        mta = sum(1 for v in answer_key.values() if v == "MTA")
        print(f"    MCQ: {mcq}  |  MSQ: {msq}  |  NAT: {nat}  |  MTA: {mta}")

    return answer_key


