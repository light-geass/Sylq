def build_page_question_map(pdf_path: str) -> dict[int, list[str]]:
    """
    Find which question numbers START on each page.
    Scans the full text line by line per page for patterns like:
        Q.1, Q.11, Q. 11, Q11  (the real PDF uses all of these)
    Returns: {page_num: ["1", "2", ...]}
    """
    page_qnums: dict[int, list[str]] = {}
    # Pattern: "Q." optionally followed by space, then 1-2 digits
    # Must be followed by a word boundary to avoid matching range headers in some cases
    q_start = re.compile(r"Q\.\s*(\d{1,2})\b")

    with pdfplumber.open(pdf_path) as pdf:
        for page_num, page in enumerate(pdf.pages, start=1):
            text = page.extract_text() or ""
            found = []
            for line in text.split("\n"):
                # Skip section headers like "Q.1 – Q.5 Carry ONE mark"
                if re.search(r"carry|mark|each", line, re.I):
                    continue
                for m in q_start.finditer(line):
                    qn = m.group(1)
                    if qn not in found:
                        found.append(qn)
            if found:
                page_qnums[page_num] = found
    return page_qnums


def build_question_page_ranges(page_qnums: dict[int, list[str]], total_pages: int) -> dict[str, tuple[int, int]]:
    """
    Build question -> (start_page, end_page) map.
    A question continues until the page before the next question starts.
    """
    starts: list[tuple[str, int]] = []
    for page in sorted(page_qnums.keys()):
        for qn in page_qnums[page]:
            starts.append((qn, page))

    ranges: dict[str, tuple[int, int]] = {}
    for idx, (qn, start_page) in enumerate(starts):
        end_page = (starts[idx + 1][1] - 1) if idx + 1 < len(starts) else total_pages
        if end_page < start_page:
            end_page = start_page
        ranges[qn] = (start_page, end_page)
    return ranges


def extract_questions_from_text(raw_text: str) -> list[dict]:
    """
    Parse raw PDF text into structured question dicts.

    The real GATE DA 2025 PDF uses:
      - "Q.1 Courage : Bravery ..."          (Q dot number)
      - "Q.11 Suppose X and Y ..."           (Q dot number, no space)
      - "Q. 11 – Q. 35 carry one mark each"  (section header — must skip)

    Strategy: find all "Q.N" at the start of lines, then extract the text
    between consecutive Q-markers.
    """
    questions = []

    # Find all question start positions.
    # Pattern: line starts (after optional whitespace) with Q. followed by number
    # We use a more flexible boundary to handle cases where text starts immediately after Q.N
    q_positions = []  # list of (char_offset, q_number)
    for m in re.finditer(r"(?:^|\n)\s*Q\.\s*(\d{1,2})\b", raw_text):
        # Check the rest of the line — skip if it's a marks header
        line_end = raw_text.find("\n", m.end())
        if line_end == -1:
            line_end = len(raw_text)
        rest_of_line = raw_text[m.end():line_end]
        if re.search(r"carry|mark.*each|–\s*Q\.|to\s*Q\.", rest_of_line, re.I):
            continue  # This is a section header, not a question
        q_positions.append((m.start(), m.group(1)))

    # Extract question bodies between consecutive Q markers
    for idx, (pos, q_num) in enumerate(q_positions):
        # Body starts after "Q.N "
        body_start = raw_text.index(q_num, pos) + len(q_num)
        body_end = q_positions[idx + 1][0] if idx + 1 < len(q_positions) else len(raw_text)
        q_body = raw_text[body_start:body_end].strip()

        if not q_body or len(q_body) < 15:
            continue

        # Remove page footers like "Organizing Institute: IIT Roorkee Page X of 34"
        q_body = re.sub(r"Organizing Institute:.*?Page \d+ of \d+", "", q_body).strip()
        # Remove next-page header like "Data Science & Artificial Intelligence - DA"
        q_body = re.sub(r"Data Science.*?- DA", "", q_body).strip()
        # Remove GATE2025 header
        q_body = re.sub(r"GATE\s*2025.*?-\s*DA", "", q_body).strip()

        if len(q_body) < 15:
            continue

        # Detect MSQ
        is_msq = bool(re.search(r"one or more|select all|multiple.*correct", q_body, re.I))

        # Extract options (A) (B) (C) (D)
        opt_re = re.compile(r"\(([A-D])\)\s*(.*?)(?=\([A-D]\)|\Z)", re.DOTALL)
        opt_matches = opt_re.findall(q_body)

        if opt_matches:
            options = [clean_math_text(m[1].strip()) for m in opt_matches]
            q_type  = QuestionType.MSQ if is_msq else QuestionType.MCQ
            first_opt_pos = q_body.find(f"({opt_matches[0][0]})")
            q_text = q_body[:first_opt_pos].strip()
        else:
            options = None
            q_type  = QuestionType.NAT
            q_text  = q_body

        q_text = clean_math_text(re.sub(r"\s+", " ", q_text).strip())
        if len(q_text) < 15:
            continue

        questions.append({
            "number":        q_num,
            "text":          q_text,
            "options":       options,
            "question_type": q_type,
        })

    return questions


# ═══════════════════════════════════════════════════════════════════════════════
#  6. IMAGE ↔ QUESTION ASSOCIATION
# ═══════════════════════════════════════════════════════════════════════════════

def associate_images(
    questions: list[dict],
    page_images: dict[int, list[dict]],
    page_qnums: dict[int, list[str]],
    pdf_path: str,
) -> dict[str, dict]:
    """
    Map each question number to its associated figure (if any).
    Logic:
      - Image on page P → find which Q numbers start on page P
      - If Q text references figure/diagram/graph → assign image to that Q
      - Otherwise assign to first Q on the same page that has no image yet
    Returns: {q_num: fig_dict}
    """
    q_images: dict[str, dict] = {}
    assigned_images: set[int] = set()       # track used image ids

    # Build question page ranges (handles questions spanning multiple pages)
    max_page = max(page_images.keys()) if page_images else (max(page_qnums.keys()) if page_qnums else 1)
    q_ranges = build_question_page_ranges(page_qnums, max_page)

    # Questions that reference a figure
    fig_words = re.compile(r"\b(figure|diagram|graph|chart|image|plot|tree)\b", re.I)

    for q in questions:
        qn   = q["number"]
        q_start, q_end = q_ranges.get(qn, (None, None))
        if not q_start:
            continue

        candidate_pages = [p for p in range(q_start, q_end + 1) if p in page_images]
        if not candidate_pages:
            continue

        figs = []
        for p in candidate_pages:
            figs.extend([(p, idx, fig) for idx, fig in enumerate(page_images[p])])
        has_ref = bool(fig_words.search(q["text"]))

        if has_ref:
            assigned = False
            for page, idx, fig in figs:
                img_id = (page, idx)
                if img_id not in assigned_images:
                    q_images[qn] = fig
                    assigned_images.add(img_id)
                    assigned = True
                    break
            
            # --- Precise Vector Diagram Extraction (Fallback) ---
            # If the question references a figure but no embedded raster image was found,
            # isolate the vector graphics (lines, rects, curves) within the question bounds.
            if not assigned:
                try:
                    with pdfplumber.open(pdf_path) as pdf:
                        p = pdf.pages[q_start - 1]
                        words = p.extract_words()
                        
                        y0, y1 = None, None
                        
                        # Find bounds for Q.qn
                        for w in words:
                            if w['text'] in (f"Q.{qn}", f"Q.{qn}."):
                                y0 = w['top']
                                break
                                
                        if y0:
                            # Find y1: Stop at first option (A) or next question
                            next_qn = str(int(qn) + 1)
                            for w in words:
                                if w['top'] > y0:
                                    if w['text'] == "(A)":
                                        y1 = w['top']
                                        break
                                    if w['text'] in (f"Q.{next_qn}", f"Q.{next_qn}."):
                                        y1 = w['top']
                                        break
                            if not y1:
                                y1 = p.height - 50
                                
                            # Filter graphics between y0 and y1
                            graphics = p.lines + p.rects + p.curves
                            valid_graphics = []
                            for g in graphics:
                                gw = g.get('width', 0)
                                gh = g.get('height', 0)
                                if gw > 400 and gh > 400: # skip watermarks
                                    continue
                                if g.get('top', 0) >= y0 and g.get('bottom', p.height) <= y1:
                                    valid_graphics.append(g)
                                    
                            if valid_graphics:
                                gx0 = min(g.get('x0', p.width) for g in valid_graphics)
                                gy0 = min(g.get('top', p.height) for g in valid_graphics)
                                gx1 = max(g.get('x1', 0) for g in valid_graphics)
                                gy1 = max(g.get('bottom', 0) for g in valid_graphics)
                                
                                if gx0 < gx1 and gy0 < gy1:
                                    # Add 15px padding
                                    bbox = (
                                        max(0, gx0 - 15), 
                                        max(0, gy0 - 15), 
                                        min(p.width, gx1 + 15), 
                                        min(p.height, gy1 + 15)
                                    )
                                    im = p.crop(bbox).to_image(resolution=200)
                                    img_byte_arr = io.BytesIO()
                                    im.original.save(img_byte_arr, format='PNG')
                                    
                                    q_images[qn] = {
                                        "bytes": img_byte_arr.getvalue(),
                                        "ext": "png",
                                        "width": im.original.width,
                                        "height": im.original.height,
                                        "rect": bbox
                                    }
                except Exception as e:
                    print(f"    [WARN] Vector graphic fallback failed for Q.{qn}: {e}")

    # Second pass: unassigned images on pages with a single question
    for page, figs in page_images.items():
        qnums_on_page = page_qnums.get(page, [])
        if len(qnums_on_page) == 1:
            qn = qnums_on_page[0]
            if qn not in q_images and figs:
                for idx, fig in enumerate(figs):
                    if (page, idx) not in assigned_images:
                        q_images[qn] = fig
                        assigned_images.add((page, idx))
                        break

    return q_images


