#  1. MATH UNICODE NORMALIZER
# ═══════════════════════════════════════════════════════════════════════════════

MATH_MAP = {
    # Greek lowercase
    "\u03b1": "alpha", "\u03b2": "beta",   "\u03b3": "gamma",
    "\u03b4": "delta", "\u03b5": "epsilon", "\u03b7": "eta",
    "\u03b8": "theta", "\u03bb": "lambda",  "\u03bc": "mu",
    "\u03c0": "pi",    "\u03c1": "rho",     "\u03c3": "sigma",
    "\u03c4": "tau",   "\u03c6": "phi",     "\u03c9": "omega",
    # Greek uppercase
    "\u0393": "Gamma", "\u0394": "Delta",   "\u0398": "Theta",
    "\u039b": "Lambda","\u03a3": "Sigma",   "\u03a6": "Phi",
    "\u03a9": "Omega",
    # Operators & relations
    "\u221a": "sqrt",  "\u221e": "inf",     "\u2202": "partial",
    "\u222b": "integral", "\u2211": "sum",  "\u220f": "product",
    "\u2248": "~=",    "\u2260": "!=",      "\u2264": "<=",
    "\u2265": ">=",    "\u00b1": "+/-",      "\u00d7": "*",
    "\u00f7": "/",     "\u00b2": "^2",       "\u00b3": "^3",
    # Arrows
    "\u2192": "->",    "\u2190": "<-",      "\u21d2": "=>",
    # Logic / sets
    "\u2208": " in ",  "\u2209": " not in ","\u2229": " ∩ ",
    "\u222a": " ∪ ",   "\u2282": " ⊂ ",     "\u00ac": "NOT ",
    "\u2227": " AND ", "\u2228": " OR ",
    "\u2200": "for all ", "\u2203": "exists ",
    "\u2286": " subseteq ", "\u2287": " supseteq ",
    # Dashes
    "\u2013": "-",     "\u2014": "-",
}


def clean_math_text(text: str) -> str:
    """Replace Unicode math symbols with readable ASCII equivalents."""
    if not text:
        return text
    text = text.replace("\uFFFD", " ")  # Unicode replacement char
    for char, repl in MATH_MAP.items():
        text = text.replace(char, repl)
    text = unicodedata.normalize("NFC", text)
    # Collapse noisy punctuation sequences produced by OCR/text extraction.
    text = re.sub(r"\?{2,}", "?", text)
    text = re.sub(r"[ \t]{2,}", " ", text)
    text = re.sub(r"\n{3,}", "\n\n", text)
    return text.strip()


def extract_best_page_text(pdf_path: str) -> str:
    """
    Extract page text using both pdfplumber and PyMuPDF, and keep the better one.
    Heuristic: fewer unknown placeholders ('?' / replacement chars) wins.
    """
    all_pages: list[str] = []
    with pdfplumber.open(pdf_path) as pdf, fitz.open(pdf_path) as doc:
        for i, page in enumerate(pdf.pages):
            p_text = page.extract_text() or ""
            f_text = doc[i].get_text("text", sort=True) or ""

            # Count unknowns in RAW text (before cleaning)
            p_unknown = p_text.count("?") + p_text.count("\uFFFD")
            f_unknown = f_text.count("?") + f_text.count("\uFFFD")

            # Prefer lower unknown count; tie-break by longer non-whitespace content.
            # Prefer pdfplumber (p_text) on ties as it often handles character spacing better.
            choose_fitz = (f_unknown < p_unknown) or (
                f_unknown == p_unknown and len(f_text.replace(" ", "")) > len(p_text.replace(" ", ""))
            )
            raw_best = f_text if choose_fitz else p_text
            best = clean_math_text(raw_best)
            all_pages.append(best)
    return "\n".join(all_pages)


# ═══════════════════════════════════════════════════════════════════════════════
#  2. MARKS PARSER  — reads section headers from PDF text
# ═══════════════════════════════════════════════════════════════════════════════

def parse_marks_map(raw_text: str) -> dict[str, int]:
    """
    Parse section headers from the ACTUAL Gate DA 2025 PDF:
        'Q.1 – Q.5 Carry ONE mark Each'       (page 1)
        'Q.6 – Q.10 Carry TWO marks Each'     (page 5)
        'Q. 11 – Q. 35 carry one mark each.'   (page 10)
        'Q. 36 – Q. 65 carry two marks each.'  (page 17)
    Returns: {"1": 1, "2": 1, ..., "36": 2, ...}
    """
    marks_map: dict[str, int] = {}

    # Flexible pattern that handles:
    #   Q.1 – Q.5    (no space after dot)
    #   Q. 11 – Q. 35  (space after dot)
    #   Q.1 to Q.25    ("to" instead of dash)
    #   – or - or —     (various dash types)
    pattern = re.compile(
        r"Q\.\s*(\d{1,2})\s*"       # Q. start_num
        r"[–\-—]+\s*"               # dash (en-dash, hyphen, em-dash)
        r"Q\.\s*(\d{1,2})\s+"       # Q. end_num
        r".*?"                       # anything in between
        r"\b(one|two|1|2)\s*mark",  # one/two/1/2 mark
        re.IGNORECASE,
    )

    for m in pattern.finditer(raw_text):
        start = int(m.group(1))
        end   = int(m.group(2))
        word  = m.group(3).lower()
        val   = {"one": 1, "two": 2, "1": 1, "2": 2}.get(word, 1)
        for n in range(start, end + 1):
            marks_map[str(n)] = val

    if marks_map:
        return marks_map

    # Fallback: standard GATE structure (30×1mk + 35×2mk)
    print("  [INFO] Could not parse marks sections — using default GATE layout")
    for n in range(1, 36):
        marks_map[str(n)] = 1
    for n in range(36, 66):
        marks_map[str(n)] = 2
    return marks_map


